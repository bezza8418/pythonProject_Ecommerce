"""
Модуль с классами-миксинами.
"""


class CreationMixin:
    """
    Миксин для логирования создания объектов.
    При инициализации выводит информацию о созданном объекте.
    """

    def __init__(self, *args, **kwargs) -> None:
        """Инициализация миксина с логированием."""
        # Сначала вызываем родительский __init__
        super().__init__(*args, **kwargs)
        # Потом логируем (атрибуты уже должны быть установлены)
        print(f"Создан объект: {self.__class__.__name__}{self._get_params()}")

    def _get_params(self) -> str:
        """
        Возвращает строку с параметрами объекта для логирования.
        Переопределяется в наследниках при необходимости.
        """
        return ""
